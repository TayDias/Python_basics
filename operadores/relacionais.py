# OPERADORES RELACIONAIS - BINÁRIOS

x = 7
y = 5

print(x > y)
print(x >= y)
print(x < y)
print(x <= y)
print(x == y)
print(x != y)


# Tipos diferentes - True
print('5' != 5)


# Sobre operadores relacionais:
#
# - Opera com dois operandos
# - O resultado é sempre True ou False
# - Não existe estritamente igual em python (x === y)
# - Em JS ('5' == 5) é True e ('5' === 5) é False