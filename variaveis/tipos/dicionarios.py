# DICIONÁRIO

aluno = {
    'nome': 'Taynara Dias',
    'nota': 9.1,
    'ativo': True
}

print(type(aluno)) # dict
print(aluno)

# Acessar uma chave
print(aluno['nome'])

# Retorna a quantidade de chaves
print(len(aluno))


